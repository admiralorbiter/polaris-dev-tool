class TestTechDebtExporterExport:
    """Tests for TechDebtExporter.export() — file writing and stats."""

    def test_export_writes_file_and_records(self, app, db):
        """Export writes markdown file and creates ExportLog."""
        from exporters.tech_debt_exporter import TechDebtExporter

        db.session.add(
            WorkItem(
                project="test",
                source_id="TD-001",
                title="Test item",
                category="tech_debt",
            )
        )
        db.session.commit()

        with tempfile.TemporaryDirectory() as tmpdir:
            output_path = Path(tmpdir) / "tech_debt.md"
            exporter = TechDebtExporter()
            stats = exporter.export("test", output_path)

            assert stats["active"] == 1
            assert stats["resolved"] == 0
            assert stats["total"] == 1
            assert output_path.exists()

            content = output_path.read_text(encoding="utf-8")
            assert "TD-001" in content

            # ExportLog should be created
            log = ExportLog.query.filter_by(project="test", target="tech_debt").first()
            assert log is not None
            assert log.record_count == 1


