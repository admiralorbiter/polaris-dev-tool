# ── BaseExporter Tests ─────────────────────────────────


class TestBaseExporter:
    """Tests for BaseExporter utilities."""

    def test_is_dirty_when_never_exported(self, app, db):
        """is_dirty returns True when no prior export exists."""
        from exporters.base import BaseExporter

        exporter = BaseExporter()
        assert (
            exporter.is_dirty("test", "tech_debt", datetime.now(timezone.utc)) is True
        )

    def test_is_dirty_when_records_updated(self, app, db):
        """is_dirty returns True when records updated after last export."""
        from exporters.base import BaseExporter

        exporter = BaseExporter()
        exporter.record_export("test", "tech_debt", "/tmp/test.md", 1)

        from datetime import timedelta

        future = datetime.now(timezone.utc) + timedelta(hours=1)
        assert exporter.is_dirty("test", "tech_debt", future) is True

    def test_is_not_dirty_when_no_changes(self, app, db):
        """is_dirty returns False when no records changed since export."""
        from exporters.base import BaseExporter

        exporter = BaseExporter()
        exporter.record_export("test", "tech_debt", "/tmp/test.md", 1)

        from datetime import timedelta

        past = datetime.now(timezone.utc) - timedelta(hours=1)
        assert exporter.is_dirty("test", "tech_debt", past) is False

    def test_table_helper(self):
        """Table helper renders a valid markdown table."""
        from exporters.base import BaseExporter

        result = BaseExporter.table(["A", "B"], [["1", "2"], ["3", "4"]])
        assert "| A | B |" in result
        assert "| 1 | 2 |" in result
        assert "| --- | --- |" in result

    def test_status_badge(self):
        """Status badge maps statuses to emoji."""
        from exporters.base import BaseExporter

        assert BaseExporter.status_badge("implemented") == "✅"
        assert BaseExporter.status_badge("unknown_status") == "❓"
